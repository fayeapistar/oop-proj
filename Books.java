    private void jbtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnUpdateActionPerformed
        try {
            // TODO add your handling code here:
            
            String bookid = jTextField1.getText();
            String btitle = jTextField2.getText();
            String authorid = jTextField3.getText();
            String genreid = jTextField4.getText();
            String pyear = jTextField5.getText();
            String bid = txtbookid.getSelectedItem().toString();
            
            ps = con.prepareStatement("UPDATE books SET title = ?, author_id = ?, genre_id = ?, publishedYear = ? WHERE bookID = ?");
            
            ps.setString(1, btitle);
            ps.setString(2, authorid);
            ps.setString(3, genreid);
            ps.setString(4, pyear);
            ps.setString(5, bid);
            
            int k = ps.executeUpdate();
            if(k==1){
                JOptionPane.showMessageDialog(this, "Book Updated Successfully!");
                jTextField1.getText();
                jTextField2.getText();
                jTextField3.getText();
                jTextField4.getText();
                jTextField5.getText();
                jTextField1.requestFocus();
                LoadBookNo();
                Fetch();
            }else{
                JOptionPane.showMessageDialog(this, "Failed to Update Book.");
            }
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(Books.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jbtnUpdateActionPerformed

