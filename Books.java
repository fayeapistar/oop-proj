    public void LoadBookNo(){
        
        try {
            ps = con.prepareStatement("SELECT `bookID` FROM `books`");
            rs = ps.executeQuery();
            txtbookid.removeAllItems();
            while(rs.next()){
                txtbookid.addItem(rs.getString(1));
            }
            
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(Books.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }

    private void Fetch(){
        try {
            int q;
            ps = con.prepareStatement("SELECT * FROM books");
            rs = ps.executeQuery();
            ResultSetMetaData rss = rs.getMetaData();
            q = rss.getColumnCount();

            DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
            df.setRowCount(0);

            while(rs.next()) { // Fixed iteration of ResultSet
                Vector v2 = new Vector(); // Use capital 'V' for Vector class
                for(int a = 1; a <= q; a++) { // Changed to <= q to include the last column
                    v2.add(rs.getString("bookID")); // Get data by column index
                    v2.add(rs.getString("title"));
                    v2.add(rs.getString("author_id"));
                    v2.add(rs.getString("genre_id"));
                    v2.add(rs.getString("publishedYear"));
                }
                df.addRow(v2); // Add the vector as a new row in the table model
            }

        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(Books.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }
    
